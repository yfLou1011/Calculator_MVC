package Calculator;

public class cModel {

	//add
	public int addTwoNumbers(int firstNum, int secondNum){
		return firstNum + secondNum;
	}
	
	//sub
	public int subTwoNumbers(int firstNum, int secondNum){
		return firstNum - secondNum;
	}
	
	//mul
	public int mulTwoNumbers(int firstNum, int secondNum){
		return firstNum * secondNum;
	}
	
	//div
	public int divTwoNumbers(int firstNum, int secondNum){
		return firstNum / secondNum;
	}
}
